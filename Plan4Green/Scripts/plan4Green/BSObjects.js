/*-------------------------------------------------------
TYPE DEFINITIONS
-------------------------------------------------------*/
// bs perspective object.
function Perspective(name, description)
{
    this.name = name;
    this.description = description;
}

// bs goal object.
function Goal(name, dueDate, description, target)
{
    this.name = name;
    this.dueDate = dueDate;
    this.description = description;
    this.target = target;
}

// bs measure object.
function Measure(name, dueDate, description, target)
{
    this.name = name;
    this.dueDate = dueDate;
    this.description = description;
    this.target = target;
}